package com.github.glhez.jtools.text;

/**
 * How number are right aligned.
 *
 * @author gael.lhez
 */
public enum RightAlignNumber {
  /**
   * Number and their parent column are right aligned.
   */
  ALL,
  /**
   * Only numbers are aligned.
   */
  NUMBERS_ONLY,
  /**
   * Don't align.
   */
  NONE
}